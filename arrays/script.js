// Arrays

// Length

const fruits = ['Banana', 'lemon', 'kiwi', 'apple'];
console.log(fruits.length); // Starts with 1 NOT 0 !!

// Pop

const cars = ["Fiat", "Toyota", "Renault", "Volkswagen"];
cars.pop();
console.log(cars) // Popped Volkswagen

// Push

cars.push("Audi");
console.log(cars)


// Shift

cars.shift();
console.log(cars)

// Unshift

cars.unshift("BMW");
console.log(cars)

// Sort

console.log(cars.sort())

// Reverse

console.log(cars.reverse())